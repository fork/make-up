#!/bin/bash

# load helper
my_dir="$(dirname "$0")"
source "$my_dir/../../helper.bash"

echo
echo "  ${BLUE}TASK${NC} Node modules"
echo

ok=false

if [ ! -d "node_modules" ]; then
  # is it yarn?
  if [ -f "yarn.lock" ]; then
    echo
    echo "  ${BLUE}TASK${NC} Install node modules with yarn (yarn.lock exists)"
    echo
    
    yarn install
    ok=true
  fi

  # is it npm?
  if [ -f "package-lock.json" ]; then
    echo
    echo "  ${BLUE}TASK${NC} Install node modules with npm (package-lock.json exists)"
    echo
    
    npm install
    ok=true
  fi
fi

# try '$ npm start'
if [ -d "node_modules" ]; then
  echo
  echo "  ${BLUE}TASK${NC} Try '$ npm start'"
  echo

  npm start --if-present
  ok=true
fi

if [ "$ok" = true ]; then
  echo
  echo "  ${GREEN}SUCCESS${NC} Done"
  echo
else
  echo
  echo "  ${RED}ERROR${NC} Could not find a method to execute command"
  echo
fi