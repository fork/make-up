#!/bin/bash

# load helper
my_dir="$(dirname "$0")"
source "$my_dir/../../helper.bash"

echo
echo "$I18N_TASK Node modules"
echo

ok=false

if [ ! -d "node_modules" ]; then
  # is it yarn?
  if [ -f "yarn.lock" ]; then
    echo
    echo "$I18N_TASK Install node modules with yarn (yarn.lock exists)"
    echo

    yarn install
    ok=true
  fi

  # is it npm?
  if [ -f "package-lock.json" ]; then
    echo
    echo "$I18N_TASK Install node modules with npm (package-lock.json exists)"
    echo

    npm install
    ok=true
  fi
fi

# try '$ npm start'
deploycommand=$(npm start --if-present)
if [ -n "$deploycommand" ]; then
  echo
  echo "$I18N_TASK Run '$ npm start'"
  echo

  npm start

  ok=true
fi

# more-make-up
MORE_MAKE_UP="${0/make-up/more-make-up}"
if [ -f "$MORE_MAKE_UP" ]; then
  echo
  echo "$I18N_TASK Run more Make-up from $MORE_MAKE_UP"
  echo

  $MORE_MAKE_UP

  echo
  echo "$I18N_SUCCESS Done"
  echo

  ok=true
fi

if [ "$ok" = true ]; then
  echo
  echo "$I18N_SUCCESS Done"
  echo
else
  echo
  echo "$I18N_ERROR Could not find a method to execute command"
  echo
fi
